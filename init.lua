local function alternateWrap()
  if vim.o.wrap then
    print 'change to nowrap'
    return 'nowrap'
  else
    print 'change to wrap'
    return 'wrap'
  end
end

-- Toggle line wrap
vim.keymap.set('n', '<M-z>', function()
  vim.cmd('set ' .. alternateWrap())
end)

